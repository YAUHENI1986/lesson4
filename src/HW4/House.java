package HW4;

public class House {
	int id = 0;
	int numberFlat;
	double areaFlat;
	int floor;
	int countRoom;
	String adress;
	String typeOfHouse;
	String dateOfMaintenance;
	
	House (int id, int numberFlat, double areaFlat, int floor, int countRoom, String adress, String typeOfHouse, String dateOfMaintenance) {
		this.id = id;
		this.numberFlat = numberFlat;
		this.areaFlat = areaFlat;
		this.floor = floor;
		this.countRoom = countRoom;
		this.adress = adress;
		this.typeOfHouse = typeOfHouse;
		this.dateOfMaintenance = dateOfMaintenance;
	}
	
		
}
